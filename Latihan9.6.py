#Program Pertukaran_Nilai

#Masukan Nilai Variable A Dan B
nilai_a = int(input("Masukan Nilai A: "))
nilai_b = int(input("Masukan Nilai B: "))

#Rumus Pertukaran Nilai
nilai_a = nilai_a + nilai_b
nilai_b = nilai_a - nilai_b
nilai_a = nilai_a - nilai_b

#Menampilkan Nilai Variable A Dan B Yang Telah Bertukar
print ("Nilai A Sekarang Adalah: ", nilai_a)
